// Text Slider Functionality
function initTextSlider() {
    const textSlider = document.querySelector('.text-slider');
    const sliderBtns = document.querySelectorAll('.slider-btn');
    
    if (textSlider && sliderBtns.length > 0) {
        let currentSlide = 0;
        const totalSlides = sliderBtns.length;
        
        // Auto-slide function
        function autoSlide() {
            currentSlide = (currentSlide + 1) % totalSlides;
            
            // Update active button
            sliderBtns.forEach((btn, index) => {
                btn.classList.toggle('active', index === currentSlide);
            });
            
            // Slide to the selected text
            textSlider.style.transform = `translateX(-${currentSlide * 16.666}%)`;
        }
        
        // Start auto-sliding every 4 seconds
        const autoSlideInterval = setInterval(autoSlide, 4000);
        
        // Manual click functionality (optional - can be removed if not needed)
        sliderBtns.forEach(btn => {
            btn.addEventListener('click', function() {
                const slideIndex = parseInt(this.getAttribute('data-slide'));
                currentSlide = slideIndex;
                
                // Update active button
                sliderBtns.forEach(b => b.classList.remove('active'));
                this.classList.add('active');
                
                // Slide to the selected text
                textSlider.style.transform = `translateX(-${slideIndex * 16.666}%)`;
                
                // Reset auto-slide timer
                clearInterval(autoSlideInterval);
                setTimeout(() => {
                    setInterval(autoSlide, 4000);
                }, 4000);
            });
        });
    }
}
